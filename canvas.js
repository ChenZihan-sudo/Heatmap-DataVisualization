var canvas2 = document.getElementById("secondCanvas");
var ctx2 = canvas2.getContext("2d");
var image = document.getElementById("source");

var picRatio = image.height / image.width; //计算图片横纵比

var needWidth = 860; //需要的图像宽度
var needHeight = 607; //通过计算得出图像高度

var keepRatio = false; //保持横纵比

if (keepRatio) {
    needHeight = picRatio * needWidth;
}

var dpr = window.devicePixelRatio; //CSS与画布分辨率之比 dpr

//设置CSS(实际)宽高
canvas2.style.width = needWidth + 'px';
canvas2.style.height = needHeight + 'px';

//设置画布宽高分辨率
canvas2.height = needHeight * dpr;
canvas2.width = needWidth * dpr;

//图像居中处理, 设置高度需要的偏移
var ratioHeight = picRatio * needWidth;
var offsetHeight = -(ratioHeight - needHeight);
var offsetWidth = 0;
var scalePic = 1;

if (offsetHeight > 0) {
    offsetHeight = 0; //取消设置高度需要的偏移
    scalePic = canvas2.height / (picRatio * canvas2.width); //设置图片缩放率
    //居中图像，设置宽度需要的偏移
    var ratioWidth = needHeight / picRatio;
    offsetWidth = -(ratioWidth - needWidth);
}

console.log("居中偏移校准", offsetWidth, offsetHeight);
console.log("canvas宽高", canvas2.width, canvas2.height);

image.onload = () => {
    ctx2.drawImage(image, offsetWidth, offsetHeight, canvas2.width * scalePic, picRatio * canvas2.width * scalePic);

    //经纬度比例数据 根据图像定义
    var ratioGeoPixelData = {
        left: 0.03111111111111111,
        right: 0.945925925925926,
        top: 0.008592910848549946,
        bottom: 0.9871106337271751
    }

    //横向线 经度线 <-> 多条件设置 分情况
    //needHeight / picRatio * dpr    =>使位置跟随图片修正而不是跟随canvas像素位置修正
    var lonPixelLeft = ratioGeoPixelData.left * (needHeight / picRatio * dpr) + offsetWidth;
    var lonPixelRight = ratioGeoPixelData.right * (needHeight / picRatio * dpr) + offsetWidth;
    if (offsetWidth == 0) {
        lonPixelLeft = ratioGeoPixelData.left * canvas2.width;
        lonPixelRight = ratioGeoPixelData.right * canvas2.width;
    }
    //纵向线 纬度线 多条件设置 分情况
    var latPixelTop = ratioGeoPixelData.top * (needWidth * picRatio * dpr) + (offsetHeight);
    //或 var latPixelTop = ratioGeoPixelData.top * canvas2.height * (offsetHeight / 2)  (特殊，不可取)
    var latPixelBottom = ratioGeoPixelData.bottom * (needWidth * picRatio * dpr) + (offsetHeight);
    if (offsetWidth != 0) {
        latPixelTop = ratioGeoPixelData.top * canvas2.height;
        latPixelBottom = ratioGeoPixelData.bottom * canvas2.height;
    }

    //横向线 经度线 <->
    drawLine(lonPixelLeft, 0, lonPixelLeft, canvas2.height);
    drawLine(lonPixelRight, 0, lonPixelRight, canvas2.height);
    //纵向线 纬度线
    drawLine(0, latPixelTop, canvas2.width, latPixelTop);
    drawLine(0, latPixelBottom, canvas2.width, latPixelBottom);

    //设置经纬度映射 固定数据
    var geoData = {
        left: 73.49932602375318,
        right: 134.76695944754232,
        top: 53.560935089181136,
        bottom: 18.15765542121043
    }

    function geoToPixelProjection(lat, lon) {

        var isLatArea = lat >= geoData.left && lat <= geoData.right;
        var isLonArea = lon >= geoData.bottom && lon <= geoData.top;

        console.log(lat, lon, isLatArea, isLonArea);

        if (isLatArea && isLonArea) {
            //经度-> 转平面像素坐标 分情况 
            var pixelDataX = (ratioGeoPixelData.right - ratioGeoPixelData.left) * (needHeight / picRatio * dpr);
            if (offsetWidth == 0) {
                pixelDataX = (ratioGeoPixelData.right - ratioGeoPixelData.left) * canvas2.width;
            }
            var pixelX = (lat - geoData.left) / (geoData.right - geoData.left) * pixelDataX;

            //纬度-> 墨卡托投影转换至平面像素坐标
            var topProjection = Math.log(Math.tan(((geoData.top / 2) + 45) * (Math.PI / 180)));
            var bottomProjection = Math.log(Math.tan(((geoData.bottom / 2) + 45) * (Math.PI / 180)));
            var currentProjection = Math.log(Math.tan(((lon / 2) + 45) * (Math.PI / 180)));

            //纬度-> 转平面像素坐标 分情况
            var pixelDataY = (ratioGeoPixelData.bottom - ratioGeoPixelData.top) * (needWidth * picRatio * dpr);
            if (offsetWidth != 0) {
                pixelDataY = (ratioGeoPixelData.bottom - ratioGeoPixelData.top) * canvas2.height;
            }
            var pixelY = (currentProjection - topProjection) / (bottomProjection - topProjection) * pixelDataY;

            //添加顶部和左侧边界限制
            pixelX += lonPixelLeft;
            pixelY += latPixelTop;

            return { pixelX, pixelY };
        } else {
            return false;
        }
    }

    //生成随机数据
    for (let i = 0; i < dotData.length; i++) {
        let lon = dotData[i].lat;
        let lat = dotData[i].lng;
        drawCircle(geoToPixelProjection(lat, lon), Math.random() > 0.99 ? 1 : 2);
    }

    function drawCircle(geo2, t) {
        if (t == 1) {
            ctx2.beginPath();
            ctx2.shadowOffsetX = 0;
            ctx2.shadowOffsetY = 4;
            ctx2.shadowColor = 'rgba(0, 0, 0, 0)';
            ctx2.shadowBlur = 10;
            
            ctx2.fillStyle = "rgba(255, 255, 255, 0)";
            ctx2.arc(geo2.pixelX, geo2.pixelY, 6, 0, 2 * Math.PI);
            ctx2.fill();

            ctx2.beginPath();
            ctx2.fillStyle = "rgba(255, 98, 0, 0.6)";
            ctx2.arc(geo2.pixelX, geo2.pixelY, 4, 0, 2 * Math.PI);
            ctx2.fill();

        } else {
            ctx2.beginPath();
            ctx2.shadowOffsetX = 0;
            ctx2.shadowOffsetY = 4;
            ctx2.shadowColor = 'rgba(0, 0, 0, 0)';
            ctx2.shadowBlur = 10;

            ctx2.fillStyle = "rgba(255, 255, 255, 0)";
            ctx2.arc(geo2.pixelX, geo2.pixelY, 6, 0, 2 * Math.PI);
            ctx2.fill();

            ctx2.beginPath();
            ctx2.fillStyle = "rgba(122, 191, 113, 0.8)";
            ctx2.arc(geo2.pixelX, geo2.pixelY, 4, 0, 2 * Math.PI);
            ctx2.fill();

        }
    }

    function drawLine(bx, by, ex, ey) {
        ctx2.beginPath();
        ctx2.lineWidth = 2;
        ctx2.strokeStyle = "#ff0000";
        ctx2.moveTo(bx, by);
        ctx2.lineTo(ex, ey);
        ctx2.stroke();
    }
}




